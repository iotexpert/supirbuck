
def chooseResistor(desired):
    return min(resitorList, key=lambda x:abs(x-desired))

def findResistorStack(desiredRatio,minCurrent=0,maxCurrent=1000000000,vin=0):
    result = set()
    best = 1000000000000
    for i in resitorList:
        for j in resitorList:
            calcRatio = i/j
            error = abs(desiredRatio - calcRatio )

            ignore = False
            calcCurrent = vin/(i+j)
            if calcCurrent < minCurrent:
                ignore = True
            if calcCurrent > maxCurrent:
                ignore = True

            if error < best and not ignore:
                result = set()
                result.add((i,j))
                best = error
            if error == best and not ignore:
                result.add((i,j))
    return result

def lowestResistance(setOfResistors):
    rval = ()
    lowest = 1e20
    for i in setOfResistors:
        if i[0] + i[1] < lowest:
            lowest = i[0] + i[1]
            rval = i
    return rval

def largestResistance(setOfResistors):
    rval = ()
    highest = 0
    for i in setOfResistors:
        if i[0] + i[1] > highest:
            highest = i[0] + i[1]
            rval = i
    return rval
    
